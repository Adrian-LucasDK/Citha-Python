#Criação  de um lista
lista = [10, 20, 30, 40, 50]

#Criação de uma lista vazia
listadobro = []

#Dobrando os valores da lista
for item in lista:
    listadobro.append(item * 2)

#Imprimindo a lista original e a lista dobrada
print("-------------------Saída-------------------")
print("Lista original: ", lista)
print("Lista dobrada: ", listadobro)
print("-------------------------------------------")